package datatypes;

public class PrimitiveDataTypes {

	public static void main(String[] args) {

		//Integer Data Type - byte, short, int, long
		byte byteData = 120;
		short shortData = 9970;
		int integerData = 9897032;
		long longData = 2147483647;

		//Boolean Data Type
		boolean boolTrue = true;
		boolean boolFalse = false;

		//Floating Point Type - float, double
		float floatingPoint = 3.14f;
		double dbl = 222.105;

		//Character Data Type
		char character = 'c';

		//Calling all data types
		System.out.println("Byte Data: " + byteData);
		System.out.println("Short Data: " + shortData);
		System.out.println("Integer Data: " + integerData);
		System.out.println("Long Data: " + longData);

		System.out.println("****************************");

		System.out.println("Boolean Data: " + boolTrue);
		System.out.println("Boolean Data: " + boolFalse);

		System.out.println("****************************");

		System.out.println("Floting Point Data: " + floatingPoint);
		System.out.println("Double Data: " + dbl);

		System.out.println("****************************");

		System.out.println("Character Data: " + character);

	}
}
