package operators;

public class RelationalOperators {

	public static void main(String[] args) {

		//Relational Operators : >, <, >=, <=, !=, ==  : Returns boolean value

		int val1 = 50;
		int val2 = 20;

		// 1.Greater then
		System.out.println(val1>val2);

		// 2.Less then
		System.out.println(val1<val2);

		// 3.Greater then equals to
		System.out.println(val1>=val2);

		// 4.Less then equals to
		System.out.println(val1<=val2);

		// 5.Not equals to
		System.out.println(val1!=val2);

		// 6.Equals to
		System.out.println(val1==val2);

	}

}
